package net.geoprism.registry.etl;

public class JSONFormatException extends JSONFormatExceptionBase
{
  @SuppressWarnings("unused")
  private static final long serialVersionUID = 1686989634;
  
  public JSONFormatException()
  {
    super();
  }
  
  public JSONFormatException(java.lang.String developerMessage)
  {
    super(developerMessage);
  }
  
  public JSONFormatException(java.lang.String developerMessage, java.lang.Throwable cause)
  {
    super(developerMessage, cause);
  }
  
  public JSONFormatException(java.lang.Throwable cause)
  {
    super(cause);
  }
  
}
